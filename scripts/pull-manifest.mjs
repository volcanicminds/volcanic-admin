#!/usr/bin/env node
/**
 * Pull the manifest from a Volcanic backend (or a local file) into a project as a
 * committed snapshot, and scaffold the overrides file (ADM-2).
 *
 * Usage:
 *   volcanic-admin-pull --url http://localhost:2230 [--token <bearer>] [--out ./src]
 *   volcanic-admin-pull --from ./manifest.json [--out ./src]
 *
 * Writes:
 *   <out>/manifest.generated.ts   (always overwritten — never edit by hand)
 *   <out>/manifest.overrides.ts   (scaffolded once if absent — your customizations)
 *
 * The fetched manifest is validated against manifest.v2.schema.json (Ajv) before writing.
 */
import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs'
import { resolve, dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import Ajv2020Module from 'ajv/dist/2020.js'
import addFormatsModule from 'ajv-formats'

const Ajv2020 = Ajv2020Module.default ?? Ajv2020Module
const addFormats = addFormatsModule.default ?? addFormatsModule
const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')

function parseArgs(argv) {
  const a = {}
  for (let i = 0; i < argv.length; i++) {
    const k = argv[i]
    if (k.startsWith('--')) a[k.slice(2)] = argv[i + 1]?.startsWith('--') || argv[i + 1] === undefined ? true : argv[++i]
  }
  return a
}

const args = parseArgs(process.argv.slice(2))
const outDir = resolve(process.cwd(), args.out ?? 'src')

async function getManifest() {
  if (args.from) return JSON.parse(readFileSync(resolve(process.cwd(), args.from), 'utf8'))
  if (args.url) {
    const headers = { Accept: 'application/json' }
    if (args.token) headers.Authorization = `Bearer ${args.token}`
    const res = await fetch(`${String(args.url).replace(/\/$/, '')}/admin/manifest`, {
      headers,
      credentials: 'include'
    })
    if (!res.ok) throw new Error(`Manifest fetch failed: ${res.status} ${res.statusText}`)
    return res.json()
  }
  throw new Error('Provide --url <apiUrl> or --from <file>')
}

function validate(manifest) {
  const schema = JSON.parse(readFileSync(join(root, 'manifest.v2.schema.json'), 'utf8'))
  const ajv = new Ajv2020({ allErrors: true, strict: true })
  addFormats(ajv)
  const ok = ajv.compile(schema)(manifest)
  if (!ok) {
    const errs = ajv.errors?.map((e) => `  ${e.instancePath || '(root)'} ${e.message}`).join('\n')
    throw new Error(`Manifest failed v2 validation:\n${errs}`)
  }
}

const GENERATED_HEADER =
  '// AUTO-GENERATED by `volcanic-admin-pull` — do not edit. Customize manifest.overrides.ts instead.\n'

const OVERRIDES_SCAFFOLD = `import type { ManifestOverrides } from '@volcanicminds/admin'
import type { GeneratedFieldMap } from './manifest.generated'

/**
 * Project overrides — merged onto manifest.generated.ts by (resource, field).
 * Survives regeneration. Patch resources/fields/capabilities, add/exclude items.
 * Typed against GeneratedFieldMap → field-name autocomplete + typo checking.
 */
export const overrides: ManifestOverrides<GeneratedFieldMap> = {}
`

/** Emit a `resource → field-name union` interface so overrides get per-resource
 *  field-name autocomplete and compile-time typo checking (see ManifestOverrides<FM>). */
function fieldMapType(manifest) {
  const lines = (manifest.resources ?? []).map((r) => {
    const names = (r.fields ?? []).map((f) => `'${f.name}'`)
    return `  ${JSON.stringify(r.name)}: ${names.length ? names.join(' | ') : 'never'}`
  })
  // A `type` alias (not `interface`) — object-literal type aliases get an implicit
  // index signature, so GeneratedFieldMap satisfies the `FieldMap` constraint.
  return `\n/** Resource → its field names (for ManifestOverrides<GeneratedFieldMap>). */\nexport type GeneratedFieldMap = {\n${lines.join('\n')}\n}\n`
}

async function main() {
  const manifest = await getManifest()
  validate(manifest)
  if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true })

  const generatedPath = join(outDir, 'manifest.generated.ts')
  writeFileSync(
    generatedPath,
    `${GENERATED_HEADER}import type { Manifest } from '@volcanicminds/admin'\n\nexport const generatedManifest = ${JSON.stringify(manifest, null, 2)} as Manifest\n${fieldMapType(manifest)}`
  )
  console.log(`✓ wrote ${generatedPath} (${manifest.resources?.length ?? 0} resources)`)

  const overridesPath = join(outDir, 'manifest.overrides.ts')
  if (existsSync(overridesPath)) {
    console.log(`• kept existing ${overridesPath}`)
  } else {
    writeFileSync(overridesPath, OVERRIDES_SCAFFOLD)
    console.log(`✓ scaffolded ${overridesPath}`)
  }
}

main().catch((e) => {
  console.error(`✖ ${e.message}`)
  process.exit(1)
})
